# Numbers
# Basic Operations
print(3 + 5)
print(7 - 2)
print(25 / 5)
print(10 * 2)
print(10 % 3)
print(10 + 2 * 2)
print((10 + 2) * 2)

# concatenation of int with string
my_number = 4
print(my_number)
print(str(my_number) + ' is my favourite number')  # str function that converts int to string

# basic functions
print(abs(my_number))  # absolute number
print(pow(2, 3))       # 2^3
print(max(2, 6, 9, 1))   # maximum or largest number
print(min(2, 6, 9, 1))   # minimum or smallest number
print(round(3.4))       # round off the numbers

from math import *   # in order to use more math function (math module)
print(floor(3.7))    # always print the lowest
print(ceil(3.7))     # always print the largest
print(sqrt(49))      # square root of a number
